package com.company;

public class Dimensions {
    private int width;
    private int height;
    private int death;

    public Dimensions(int width, int height, int death) {
        this.width = width;
        this.height = height;
        this.death = death;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getDeath() {
        return death;
    }
}
